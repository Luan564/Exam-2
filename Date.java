
public class Date {
    private int year;
    private int month;
    
    public Date(int year, int month){
        this.month=month;
        this.year=year;
    }
    public int getMonth() {
        return month;
    }
    public int getYear() {
        return year;
    }
    public void setMonth(int month) {
        this.month = month;
    }
    public void setYear(int year) {
        this.year = year;
    }

}
